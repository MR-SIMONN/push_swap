#include "libft.h"

int	ft_lstsize(t_slist *lst)
{
	int		i;
	t_slist	*p;

	if (!lst)
		return (0);
	i = 0;
	p = lst;
	while (p)
	{
		i++;
		p = p->next;
	}
	return (i);
}
